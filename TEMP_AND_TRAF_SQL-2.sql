--QUESTION 2:Does temperature impact vehicle speed?
SELECT T.AVERAGE_TEMPERATURE,
AVG(TR.AVERAGE_SPEED) AS AVG_SPEED
FROM TEMPERATURE T 
JOIN TRAFFIC TR
ON T.DAYS=TR.DAYS
GROUP BY T.AVERAGE_TEMPERATURE
ORDER BY AVG_SPEED DESC;


SELECT 
 CASE 
  WHEN T.AVERAGE_TEMPERATURE< 15 THEN 'LOW TEMP'
  WHEN T.AVERAGE_TEMPERATURE BETWEEN 15 AND 25 THEN 'MEDIUM TEMP'
  ELSE 'HIGH TEMP'
 END AS TEMP_CATEGORY,
AVG(TR.AVERAGE_SPEED) AS AVG_SPEED
FROM TEMPERATURE T 
JOIN TRAFFIC TR
ON T.DAYS=TR.DAYS
GROUP BY TEMP_CATEGORY
ORDER BY AVG_SPEED DESC;
  

--QUESTION 3:What is the peak traffic pattern across the day?
SELECT TR.HOURS,
TR.CONGESTION_LEVEL,
COUNT(*) AS TRAFFIC_COUNT
FROM TRAFFIC TR
GROUP BY TR.HOURS,TR.CONGESTION_LEVEL
ORDER BY TR.HOURS,TRAFFIC_COUNT DESC;

--QUESTION 4:How do traffic violations vary with congestion level?
SELECT TR.CONGESTION_LEVEL,
TR.TRAFFIC_RULE_VIOLATION,
COUNT(*) AS COUNT_VIOLATION 
FROM TRAFFIC TR
GROUP BY TR.CONGESTION_LEVEL,TR.TRAFFIC_RULE_VIOLATION
ORDER BY COUNT_VIOLATION DESC;  

--QUESTION 5:Can you identify high-risk conditions (weather + congestion + violations)?
SELECT 
TR.WEATHER_CONDITION,
TR.TRAFFIC_RULE_VIOLATION,
COUNT(*) AS RISK_COUNT
FROM TRAFFIC TR
WHERE TR.CONGESTION_LEVEL='High'
GROUP BY TR.WEATHER_CONDITION,TR.TRAFFIC_RULE_VIOLATION
ORDER BY RISK_COUNT DESC;


